package com.twcch.test.operandandoperator;

public class TernaryOperator {

    public static void main(String[] args) {

        /*
         * 條件運算子(conditional operator); ITE 運算子
         * if (4 > 3) { a = 10; } else { a = 20; }
         */
        int a = (4 > 3) ? 10 : 20;
        System.out.println("a: " + a);

    }

}
